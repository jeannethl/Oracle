set line 180
col "Name" for a10
col "Status" format a10
col "%Used" format 999.99
col "Size GB" for 999,999,999.99
col "Used GB" for 999,999,999.99
col "Free GB" for 999,999,999.99
col "Num Archives" for 999,999
SELECT NAME "Name"
, ROUND(SPACE_LIMIT/1024/1024/1024,2) "Size GB"
, ROUND((SPACE_LIMIT-SPACE_Used)/1024/1024/1024,2) "Free GB"
, ROUND(SPACE_Used/1024/1024/1024,2) "Used GB"
, ROUND((SPACE_Used/SPACE_LIMIT)*100,2) "%Used"
, NUMBER_OF_FILES "Num Archives"
, CASE WHEN ((SPACE_Used/SPACE_LIMIT)*100 < 80) THEN 'NORMAL'
WHEN ((SPACE_Used/SPACE_LIMIT)*100 BETWEEN 80 and 90) THEN 'WARNING'
ELSE 'CRITICAL' END "Status"
FROM V$RECOVERY_FILE_DEST;


